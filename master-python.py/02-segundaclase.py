"""CALCULADORA DE EXPONENTES"""

# Bienvenida al usuario sobre el tipo de calculadora
print("CALCULADORA DE EXPONENTES")
# Datos solicitados al usuario
a = input("Introduzca el primer número\n ")
b = input("Introduzca el segúndo número\n ")
c = int(a) ** int(b)
# Mensaje de finalizacion con los datos obtenidos
print(f"El resutado de {a} elevado a la {b} es igual a {c}")
